package ch05_03;

public class MultiFor {
    public static void main(String[] args) {
        for(int x=0;x<4;x++){

            for(int y=4;y>2;y--){
                System.out.println(x+" "+y+"이세령_2021250005");
            }
            if(x==1){
                x++;
            }
        }
    }
}

